func findKthLargest(nums []int, k int) int {
	fmt.Println(k, nums)
	if len(nums) < k {
		return 0
	}
	p := find(nums)
	if k == p+1 {
		return nums[p]
	}
	if k > p {
		return findKthLargest(nums[p+1:], k-p-1)
	}
	return findKthLargest(nums[:p], k)
}

