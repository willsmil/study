func topKFrequent(nums []int, k int) []int {
	m := make(map[int]int)
	for _, n := range nums {
		m[n]++
	}
	res := make([]int, 0)
	if len(m) <= k {
		for x, _ := range m {
			res = append(res, x)
		}
		return res
	}
	for x, v := range m {
		if len(res) >= k && v < m[res[k-1]] {
			continue
		}
		if len(res) > 0 && v > m[res[0]] {
			res = append([]int{x}, res...)
		} else {
			j := 0
			for ; j < len(res); j++ {
				if m[res[j]] < v {
					break
				}
			}
			res = append(res[:j], append([]int{x}, res[j:]...)...)
		}
	}
	return res[:k]
}

